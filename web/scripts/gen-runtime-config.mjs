// Generate public/runtime-config.js from .env.local (or .env) for local `pnpm dev`.
//
// This mirrors web/docker-entrypoint.sh, which writes the same file from the
// PUBLIC_* environment at container start. `next dev` does NOT run that
// entrypoint, so without this script the committed placeholder stays empty and
// the client falls back to localhost defaults (see app/lib/runtime-config.ts).
//
// Reads PUBLIC_* keys (same names as the Docker entrypoint). Unset keys are
// left empty so the localhost fallbacks still apply. Dependency-free so it works
// the same under npm/pnpm/bun without module-resolution surprises.
import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');

/** Minimal .env parser: KEY=VALUE per line, supports # comments and quotes. */
function loadEnvFile(file) {
  const out = {};
  if (!existsSync(file)) return out;
  for (const raw of readFileSync(file, 'utf8').split('\n')) {
    const line = raw.trim();
    if (!line || line.startsWith('#')) continue;
    const eq = line.indexOf('=');
    if (eq === -1) continue;
    const key = line.slice(0, eq).trim();
    let val = line.slice(eq + 1).trim();
    if (
      (val.startsWith('"') && val.endsWith('"')) ||
      (val.startsWith("'") && val.endsWith("'"))
    ) {
      val = val.slice(1, -1);
    }
    out[key] = val;
  }
  return out;
}

// Precedence: real process env > .env.local > .env (matches Next's ordering).
const env = {
  ...loadEnvFile(join(root, '.env')),
  ...loadEnvFile(join(root, '.env.local')),
  ...process.env,
};

const get = (key, fallback = '') => (env[key] != null && env[key] !== '' ? env[key] : fallback);

// ICE_SERVERS is injected as raw JSON; guard against invalid input.
let iceServers = get('PUBLIC_ICE_SERVERS', '[]');
try {
  JSON.parse(iceServers);
} catch {
  console.warn('[gen-runtime-config] PUBLIC_ICE_SERVERS is not valid JSON; using []');
  iceServers = '[]';
}

const body = `// AUTO-GENERATED by scripts/gen-runtime-config.mjs (runs on \`pnpm dev\`).
// Edit your .env.local (PUBLIC_* keys) instead of this file.
window.__RUNTIME_CONFIG__ = {
  "API_BASE": ${JSON.stringify(get('PUBLIC_API_BASE'))},
  "GO_API_BASE": ${JSON.stringify(get('PUBLIC_GO_API_BASE', get('PUBLIC_API_BASE')))},
  "SIGNAL_URL": ${JSON.stringify(get('PUBLIC_WS_URL'))},
  "BRIDGE_URL": ${JSON.stringify(get('PUBLIC_BRIDGE_URL'))},
  "BRIDGE_TOKEN": ${JSON.stringify(get('PUBLIC_BRIDGE_TOKEN'))},
  "ICE_SERVERS": ${iceServers}
};
`;

writeFileSync(join(root, 'public', 'runtime-config.js'), body);
console.log('[gen-runtime-config] wrote public/runtime-config.js');
